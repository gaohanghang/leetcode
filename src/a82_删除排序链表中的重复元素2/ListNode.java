package a82_删除排序链表中的重复元素2;

import java.util.List;

/**
 * @Description
 * @Author Gao Hang Hang
 * @Date 2020-02-16 22:43
 **/
public class ListNode {
    int val;
    ListNode next;

    ListNode(int x) {
        val = x;
    }

    ListNode(int val, ListNode next) {
        this.val = val;
        this.next = next;
    }
}
